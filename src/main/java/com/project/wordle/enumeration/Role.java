package com.project.wordle.enumeration;

/**
 * UserRole is an enumeration that represents the different roles that a user can have in the application.
 * It contains two roles: ADMIN and EMPLOYEE.
 *
 * @author Dragan Jovanovic
 * @version 1.0
 * @since 1.0
 */
public enum Role {
    /**
     * The role of an admin user.
     */
    ADMIN,

    /**
     * The role of a regular employee user.
     */
    EMPLOYEE
}